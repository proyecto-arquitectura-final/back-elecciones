CREATE TABLE election_result_summary (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMPTZ(6),
    updated_at TIMESTAMPTZ(6),
    election_id BIGINT NOT NULL,
    eligible_voters BIGINT NOT NULL DEFAULT 0,
    total_voters BIGINT NOT NULL DEFAULT 0,
    valid_votes BIGINT NOT NULL DEFAULT 0,
    blank_votes BIGINT NOT NULL DEFAULT 0,
    null_votes BIGINT NOT NULL DEFAULT 0,
    unmarked_votes BIGINT NOT NULL DEFAULT 0,
    reported_tables INTEGER NOT NULL DEFAULT 0,
    total_tables INTEGER NOT NULL DEFAULT 0,
    source VARCHAR(255) NOT NULL DEFAULT 'CARGA_MANUAL',
    imported_at TIMESTAMPTZ(6) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT election_result_summary_election_fk
        FOREIGN KEY (election_id) REFERENCES election(id),
    CONSTRAINT election_result_summary_election_uk UNIQUE (election_id),
    CONSTRAINT election_result_summary_non_negative_ck CHECK (
        eligible_voters >= 0
        AND total_voters >= 0
        AND valid_votes >= 0
        AND blank_votes >= 0
        AND null_votes >= 0
        AND unmarked_votes >= 0
        AND reported_tables >= 0
        AND total_tables >= 0
    ),
    CONSTRAINT election_result_summary_tables_ck CHECK (reported_tables <= total_tables),
    CONSTRAINT election_result_summary_voters_ck CHECK (total_voters <= eligible_voters OR eligible_voters = 0),
    CONSTRAINT election_result_summary_vote_breakdown_ck CHECK (
        valid_votes + null_votes + unmarked_votes <= total_voters OR total_voters = 0
    )
);

CREATE INDEX idx_election_result_summary_imported_at
    ON election_result_summary(imported_at DESC);

-- El modelo representa un consolidado vigente por candidato y territorio.
-- Antes de crear la restricción, conserva el registro más reciente de cada combinación.
WITH ranked AS (
    SELECT id,
           ROW_NUMBER() OVER (
               PARTITION BY election_id,
                            candidate_id,
                            COALESCE(department, ''),
                            COALESCE(municipality, '')
               ORDER BY COALESCE(imported_at, updated_at, created_at) DESC NULLS LAST,
                        id DESC
           ) AS row_number
    FROM official_result
)
DELETE FROM official_result result
USING ranked duplicate
WHERE result.id = duplicate.id
  AND duplicate.row_number > 1;

CREATE UNIQUE INDEX uq_official_result_election_candidate_scope
    ON official_result (
        election_id,
        candidate_id,
        COALESCE(department, ''),
        COALESCE(municipality, '')
    );

CREATE INDEX idx_official_result_election_department_municipality
    ON official_result(election_id, department, municipality);
